SOH  = $01  ; start block
EOT  = $04  ; end of text marker
ACK  = $06  ; good block acknowledged
BELL = $07  ; Bell
BS   = $08  ; Backspace
LF   = $0a  ; line feed
CLS  = $0c  ; clear screen
CR   = $0d  ; carriage return
NAK  = $15  ; bad block acknowledged
CAN  = $18  ; cancel (not standard, not supported)
ESC  = $1b  ; escape
SPC  = $20  ; space

